legend('sensor 6');
grid on
legend('sensor 1','sensor 3', 'sensor 4')

axes(handles.axes3);
w = 0;
T = 5;
%t=-5*T:1:5*T;
t=0:1:5*T; 
w=1*rectpuls(t-5,T);
plot(t, w, 'b')
legend('sensor 5');
grid on

axes(handles.axes4);
w = 0;
T = 5;
%t=-5*T:1:5*T;
t=0:1:5*T; 
